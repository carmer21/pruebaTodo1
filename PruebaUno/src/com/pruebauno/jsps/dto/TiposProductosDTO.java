package com.pruebauno.jsps.dto;

public class TiposProductosDTO {
	
	private int idTiposProductos;
	private String codigo;
	private String tipo;
	
	public int getIdTiposProductos() {
		return idTiposProductos;
	}
	public void setIdTiposProductos(int idTiposProductos) {
		this.idTiposProductos = idTiposProductos;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	

}
